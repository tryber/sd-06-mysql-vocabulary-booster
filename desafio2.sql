SELECT JOB_TITLE AS Cargo,
CASE WHEN MAX_SALARY>5000 AND MAX_SALARY<=10000 THEN 'Baixo'
CASE WHEN MAX_SALARY>10001 AND MAX_SALARY<=20000 THEN 'Médio'
CASE WHEN MAX_SALARY>20001 AND MAX_SALARY<=30000 THEN 'Alto'
CASE WHEN MAX_SALARY>30000 THEN 'Altissimo'
ELSE 'Não classificado'
AS Nível FROM hr.jobs;