SELECT
UCASE(CONCAT(t1.FIRST_NAME, ' ', t1.LAST_NAME)) AS 'Nome completo',
t2.START_DATE AS 'Data de início',
t1.SALARY AS 'Salário'
FROM hr.job_history AS t2
INNER JOIN hr.employees AS t1
ON t2.EMPLOYEE_ID = t1.EMPLOYEE_ID
WHERE MONTH(t2.START_DATE) BETWEEN 01 AND 03
ORDER BY
UCASE(CONCAT(t1.FIRST_NAME, ' ', t1.LAST_NAME)),
t2.START_DATE;
