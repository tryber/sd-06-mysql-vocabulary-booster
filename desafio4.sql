SELECT  J.JOB_TITLE AS 'Cargo', AVG(Emp.SALARY) AS 'Média salarial',
CASE
WHEN AVG(Emp.SALARY) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN AVG(Emp.SALARY) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN AVG(Emp.SALARY) BETWEEN  7501 AND 10500 THEN 'Sênior'
WHEN AVG(Emp.SALARY) > 10500 THEN 'CEO'
END AS 'Senioridade'	
FROM hr.jobs AS J INNER JOIN hr.employees AS Emp  
ON J.JOB_ID = Emp.JOB_ID GROUP BY J.JOB_TITLE ORDER BY AVG(Emp.SALARY), J.JOB_TITLE;
