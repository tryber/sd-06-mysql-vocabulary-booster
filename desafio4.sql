SELECT JOB_TITLE AS `Cargo`, ROUND(((MAX_SALARY-MIN_SALARY)/2),2) AS `Média salarial`,
CASE
WHEN  ((MAX_SALARY-MIN_SALARY)/2) < 5800 THEN  'Júnior'
WHEN  ((MAX_SALARY-MIN_SALARY)/2) BETWEEN  5801 AND 7500 THEN 'Pleno'
WHEN  ((MAX_SALARY-MIN_SALARY)/2) BETWEEN  7501 AND 10500 THEN 'Sênior'
WHEN  ((MAX_SALARY-MIN_SALARY)/2) >= 10500 THEN 'CEO'
END AS `Senioridade`
FROM hr.jobs
ORDER BY `Senioridade` ASC, JOB_TITLE ASC;
